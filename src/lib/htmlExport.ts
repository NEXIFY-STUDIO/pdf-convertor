/**
 * Generate HTML for individual pages
 * @param pageAssets - The page assets
 * @param config - The export configuration
 * @returns HTML string for all pages
 */
function generatePagesHtml(pageAssets: PageAsset[], config: HtmlExportConfig): string {
  return pageAssets.map(asset => {
    const pageNumberStr = String(asset.pageNumber).padStart(3, '0');
    const imageFileName = `assets/page-${pageNumberStr}.png`;
    
    if (config.mode === 'visual') {
      // Visual fidelity mode: image with optional text layer
      let textLayerHtml = '';
      
      if (config.includeTextLayer) {
        if (asset.textLayerHtml) {
          // Visual text layer using SVG foreignObject scaling for pixel-perfect positioning
          textLayerHtml = `
        <svg class="text-layer-svg" viewBox="0 0 ${asset.width} ${asset.height}">
          <foreignObject x="0" y="0" width="${asset.width}" height="${asset.height}">
            <div xmlns="http://www.w3.org/1999/xhtml" class="text-layer" style="width: ${asset.width}px; height: ${asset.height}px;">
              ${asset.textLayerHtml}
            </div>
          </foreignObject>
        </svg>
          `;
        } else if (asset.textContent) {
          // Fallback text layer
          const escapedContent = asset.textContent.split('\n').map(line => escapeHtml(line)).join('\n');
          textLayerHtml = `
        <div class="text-layer">
          <div class="text-layer-content">
            ${escapedContent}
          </div>
        </div>
          `;
        }
      }
      
      return `
        <div class="page page-${asset.pageNumber}" id="page-${asset.pageNumber}">
          <img src="${escapeHtmlAttribute(imageFileName)}" alt="Page ${asset.pageNumber}" />
          ${textLayerHtml}
        </div>
      `;
    } else {
      // Structured mode: semantic-ish HTML
      return generateStructuredPageHtml(asset);
    }
  }).join('\n');
}

/**
 * Generate structured HTML for a page (experimental mode)
 * @param asset - The page asset
 * @param pageNumberStr - The page number as a string
 * @returns HTML string for the structured page
 */
function generateStructuredPageHtml(asset: PageAsset): string {
  const textContent = asset.textContent || '';
  
  // Split by paragraph breaks and wrap in <p> tags
  const paragraphs = textContent.split(/\n\n+/).map(para => {
    const escapedPara = escapeHtml(para).replace(/\n/g, '<br>');
    return `<p>${escapedPara}</p>`;
  }).join('\n');
  
  return `
    <article class="page page-${asset.pageNumber}" id="page-${asset.pageNumber}">
      <header class="page-header">
        <h2>Page ${asset.pageNumber}</h2>
      </header>
      <div class="page-content">
        <div class="page-text">
          ${paragraphs}
        </div>
      </div>
      <footer class="page-footer">
        <p>Page ${asset.pageNumber}</p>
      </footer>
    </article>
  `;
}

/**
 * Generate metadata for the HTML head section
 * @param title - The document title
 * @param documentInfo - The PDF document information
 * @param config - The export configuration
 * @returns HTML meta tags string
 */
function generateMetaTags(
  title: string,
  documentInfo: PdfDocumentInfo
): string {
  const description = escapeHtmlAttribute(
    `HTML export of ${documentInfo.fileName} - ${documentInfo.numPages} pages`
  );
  
  // Strict CSP as required
  const csp = `default-src 'none'; img-src 'self' data:; style-src 'self' 'unsafe-inline'; font-src 'self' data:`;
  
  return `
    <meta name="description" content="${description}">
    <meta name="author" content="PDF HTML Forge">
    <meta name="generator" content="PDF HTML Forge v1.0.0">
    <meta name="robots" content="noindex, nofollow">
    <meta http-equiv="Content-Security-Policy" content="${csp}">
    <meta name="theme-color" content="#1a1a2e">
    <meta property="og:title" content="${title}">
    <meta property="og:description" content="${description}">
    <meta property="og:type" content="website">
  `;
}

/**
 * Generate metadata JSON for the export
 * @param pageAssets - The page assets
 * @param documentInfo - The PDF document information
 * @param config - The export configuration
 * @returns Metadata object
 */
function generateMetadata(
  pageAssets: PageAsset[],
  documentInfo: PdfDocumentInfo,
  config: HtmlExportConfig
): Metadata {
  return {
    title: sanitizeFileName(documentInfo.fileName).replace(/\.pdf$/i, ''),
    author: '',
    subject: `HTML export of ${documentInfo.fileName}`,
    keywords: 'pdf, html, export, conversion',
    creator: 'PDF HTML Forge',
    producer: 'PDF HTML Forge v1.0.0',
    creationDate: new Date().toISOString(),
    modificationDate: new Date().toISOString(),
    numPages: pageAssets.length,
    fileName: documentInfo.fileName,
    fileSize: documentInfo.fileSize,
    exportDate: new Date().toISOString(),
    mode: config.mode,
    scale: config.scale,
  };
}

/**
 * Sanitize a filename for use in URLs and file systems
 * @param fileName - The filename to sanitize
 * @returns Sanitized filename
 */
function sanitizeFileName(fileName: string): string {
  return fileName
    .replace(/[^a-zA-Z0-9._-]/g, '-')
    .replace(/^-+|-+$/g, '')
    .replace(/-+/g, '-')
    .substring(0, 100);
}

/**
 * Download the export ZIP file
 * @param exportResult - The export result
 */
export function downloadExport(exportResult: ExportResult): void {
  saveAs(exportResult.zipBlob, exportResult.fileName);
}

/**
 * Revoke all object URLs from page assets
 * @param pageAssets - The page assets
 */
export function revokePageAssetUrls(pageAssets: PageAsset[]): void {
  for (const asset of pageAssets) {
    if (asset.imageUrl) {
      URL.revokeObjectURL(asset.imageUrl);
    }
  }
}

/**
 * Clean up resources after export
 * @param pageAssets - The page assets to clean up
 */
export function cleanupExportResources(pageAssets: PageAsset[]): void {
  // Revoke all object URLs
  revokePageAssetUrls(pageAssets);
}

export default {
  generateHtmlExport,
  generateHtml,
  generateCss,
  generatePagesHtml,
  generateMetadata,
  downloadExport,
  revokePageAssetUrls,
  cleanupExportResources,
};
