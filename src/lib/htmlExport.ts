/**
 * PDF HTML Forge - HTML Export Logic
 * 
 * This module handles the creation of HTML, CSS, and ZIP exports from converted PDF pages.
 * It generates static, offline-capable HTML with strict CSP.
 */

import JSZip from 'jszip';
import { saveAs } from 'file-saver';
import { 
  ConversionMode, 
  RenderScale, 
  PageAsset, 
  ExportResult,
  Metadata,
  PdfDocumentInfo,
} from '../types';
import { escapeHtml, escapeHtmlAttribute } from './escapeHtml';
import { formatFileSize } from './fileSize';

/**
 * HTML export configuration
 */
export interface HtmlExportConfig {
  mode: ConversionMode;
  scale: RenderScale;
  includeTextLayer: boolean;
  includeOriginalPdf: boolean;
  originalPdf?: File;
}

/**
 * Default HTML export configuration
 */
export const DEFAULT_EXPORT_CONFIG: HtmlExportConfig = {
  mode: 'visual',
  scale: 2,
  includeTextLayer: true,
  includeOriginalPdf: false,
};

/**
 * Generate a complete HTML export from page assets
 * @param pageAssets - The page assets to export
 * @param documentInfo - The PDF document information
 * @param config - The export configuration
 * @returns Promise that resolves to the export result
 */
export async function generateHtmlExport(
  pageAssets: PageAsset[],
  documentInfo: PdfDocumentInfo,
  config: HtmlExportConfig
): Promise<ExportResult> {
  // Create ZIP archive
  const zip = new JSZip();
  
  // Generate metadata
  const metadata = generateMetadata(pageAssets, documentInfo, config);
  
  // Add metadata to ZIP
  zip.file('metadata.json', JSON.stringify(metadata, null, 2));
  
  // Generate CSS
  const cssContent = generateCss(pageAssets, config);
  zip.file('styles.css', cssContent);
  
  // Generate HTML
  const htmlContent = generateHtml(pageAssets, documentInfo, config);
  zip.file('index.html', htmlContent);
  
  // Add page assets (images)
  const assetsFolder = zip.folder('assets');
  if (assetsFolder) {
    for (const pageAsset of pageAssets) {
      const pageNumberStr = String(pageAsset.pageNumber).padStart(3, '0');
      const fileName = `page-${pageNumberStr}.png`;
      
      // Revoke the object URL after adding to ZIP
      assetsFolder.file(fileName, pageAsset.imageBlob, { binary: true });
      URL.revokeObjectURL(pageAsset.imageUrl);
    }
  }
  
  // Add original PDF if requested
  if (config.includeOriginalPdf && config.originalPdf) {
    zip.file(config.originalPdf.name, config.originalPdf, { binary: true });
  }
  
  // Generate ZIP
  const zipBlob = await zip.generateAsync({ type: 'blob' });
  
  // Generate export filename
  const baseName = sanitizeFileName(documentInfo.fileName);
  const fileName = `${baseName.replace(/\.pdf$/i, '')}-html-export.zip`;
  
  return {
    zipBlob,
    fileName,
  };
}

/**
 * Generate HTML content for the export
 * @param pageAssets - The page assets
 * @param documentInfo - The PDF document information
 * @param config - The export configuration
 * @returns The HTML content as a string
 */
function generateHtml(
  pageAssets: PageAsset[],
  documentInfo: PdfDocumentInfo,
  config: HtmlExportConfig
): string {
  const baseName = sanitizeFileName(documentInfo.fileName);
  const title = escapeHtml(baseName.replace(/\.pdf$/i, ''));
  
  // Generate page HTML based on mode
  const pagesHtml = generatePagesHtml(pageAssets, config);
  
  // Generate metadata for HTML head
  const metaTags = generateMetaTags(title, documentInfo);
  
  const html = `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${title}</title>
  ${metaTags}
  <link rel="stylesheet" href="styles.css">
</head>
<body>
  <div class="document-container">
    <header class="document-header">
      <h1>${title}</h1>
      <p class="document-info">
        <span>${pageAssets.length} page(s)</span> | 
        <span>${formatFileSize(documentInfo.fileSize)}</span>
      </p>
    </header>
    
    <main class="document-content">
      ${pagesHtml}
    </main>
    
    <footer class="document-footer">
      <p>Generated by PDF HTML Forge | ${new Date().toISOString().split('T')[0]}</p>
    </footer>
  </div>
  
  <script>
    // Print styles
    function setupPrintStyles() {
      const style = document.createElement('style');
      style.media = 'print';
      style.textContent = 
        '.page { box-shadow: none !important; } ' +
        '.page img { max-width: 100% !important; height: auto !important; }';
      document.head.appendChild(style);
    }
    
    // Helper to toggle text overlay visibility for debugging/accessibility
    window.toggleTextOverlay = function(show) {
      document.querySelectorAll('.text-layer :is(span, br)').forEach(el => {
        el.style.color = show ? 'rgba(0,0,0,0.6)' : 'transparent';
      });
    };
    
    // Initialize
    document.addEventListener('DOMContentLoaded', setupPrintStyles);
  </script>
</body>
</html>`;

  return html;
}

